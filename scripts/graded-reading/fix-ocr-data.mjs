import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const appRoot = path.resolve(__dirname, '../..');
const dataFile = path.join(appRoot, 'src', 'data', 'gradedReadingData.ts');

// Patterns to detect copyright/non-story text
const COPYRIGHT_PATTERNS = [
  /scan\s*the\s*code/i,
  /download\s*the\s*audio/i,
  /dc\s*canada/i,
  /all\s*rights\s*reserved/i,
  /copyright\s*©/i,
  /isbn/i,
  /published\s*by/i,
  /library\s*and\s*archives/i,
];

// Patterns to clean title prefixes
function cleanTitle(title) {
  if (!title) return '';
  
  // Remove leading non-letter junk
  let cleaned = title.replace(/^[^a-zA-Z]+/, '').trim();
  
  // Remove trailing junk after title (like "-2", "-1", etc. that are page numbers)
  cleaned = cleaned.replace(/[-_]\d+$/, '').trim();
  
  // Fix common OCR errors
  const ocrFixes = {
    'aN ': '',
    'nw ': '',
    'fe ': '',
    'rd ': '',
    'AD ': '',
    'Lg u': '',
    'b= ': '',
    '| ': '',
    '>» ': '',
    'I 2.': '',
    'A 1 ]': '',
    'A 2 ]': '',
    'A 1 )': '',
    'A ': '',
    '5 ': '',
  };
  
  for (const [bad, good] of Object.entries(ocrFixes)) {
    if (cleaned.startsWith(bad)) {
      cleaned = cleaned.substring(bad.length).trim();
      break;
    }
  }
  
  // Capitalize first letter
  if (cleaned.length > 0) {
    cleaned = cleaned[0].toUpperCase() + cleaned.slice(1);
  }
  
  return cleaned;
}

function isCopyrightText(text) {
  if (!text || text.length < 20) return false;
  return COPYRIGHT_PATTERNS.some(p => p.test(text));
}

function cleanTranscript(text) {
  if (!text) return '';
  
  // Remove standalone lines that are just numbers
  let lines = text.split('\n').map(l => l.trim());
  lines = lines.filter(line => {
    if (/^\d+$/.test(line) && line.length <= 2) return false;
    return true;
  });
  
  return lines.join('\n').trim();
}

async function main() {
  console.log('Reading data file...');
  
  if (!fs.existsSync(dataFile)) {
    console.error('Data file not found:', dataFile);
    process.exit(1);
  }
  
  // Read the TS file content
  const content = fs.readFileSync(dataFile, 'utf8');
  
  // Extract the JSON data objects using regex
  const dataMatch = content.match(/gradedReadingData: Record<GradedLevel, GradedReadingStory\[\]> = ([\s\S]+?);\n\nexport const gradedReadingBooks/);
  const booksMatch = content.match(/gradedReadingBooks: Record<GradedLevel, GradedBookMeta\[\]> = ([\s\S]+?);\n\nexport const GRADED_LEVEL_META/);
  
  if (!dataMatch) {
    console.error('Could not parse gradedReadingData from file');
    process.exit(1);
  }
  
  const data = JSON.parse(dataMatch[1]);
  const books = JSON.parse(booksMatch[1]);
  
  let fixedStories = 0;
  let removedStories = 0;
  
  for (const [level, stories] of Object.entries(data)) {
    for (const story of stories) {
      const originalTitle = story.titleEn;
      const originalTranscript = story.transcriptEn;
      
      // Clean title
      story.titleEn = cleanTitle(story.titleEn);
      
      // Check if transcript is copyright text
      if (isCopyrightText(story.transcriptEn)) {
        console.log(`  [${level} B${story.book} S${story.story}] Copyright text detected, clearing: "${story.titleEn}"`);
        story.transcriptEn = '';
        story.sentences = [];
        removedStories++;
      } else {
        // Clean transcript
        story.transcriptEn = cleanTranscript(story.transcriptEn);
        // Re-split sentences
        const sentences = story.transcriptEn
          .split(/(?<=[.!?])\s+(?=[A-Z""])/)
          .map(s => s.trim())
          .filter(s => s.length > 8)
          .slice(0, 30);
        story.sentences = sentences;
      }
      
      if (story.titleEn !== originalTitle || story.transcriptEn !== originalTranscript) {
        fixedStories++;
      }
    }
    
    // Update book story counts (in case some were removed)
    for (const book of books[level]) {
      const actualCount = data[level].filter(s => s.book === book.book).length;
      book.storyCount = actualCount;
    }
  }
  
  // Re-generate the TypeScript file
  const newContent = `// This file is generated by scripts/graded-reading/build-graded-reading-data.mjs.
// Do not edit by hand.

export type GradedLevel = 'l1' | 'l2' | 'l3';

export interface GradedReadingStory {
  id: string;
  level: GradedLevel;
  book: number;
  story: number;
  monthEn: string;
  monthCn: string;
  titleEn: string;
  titleCn: string;
  audioSrc: string;
  transcriptEn: string;
  transcriptCn: string;
  sentences: string[];
  imageLeft: string;
  imageRight: string;
}

export interface GradedBookMeta {
  book: number;
  monthEn: string;
  monthCn: string;
  storyCount: number;
}

export const gradedReadingData: Record<GradedLevel, GradedReadingStory[]> = ${JSON.stringify(data, null, 2)};

export const gradedReadingBooks: Record<GradedLevel, GradedBookMeta[]> = ${JSON.stringify(books, null, 2)};

export const GRADED_LEVEL_META: Record<GradedLevel, { label: string; sublabel: string; accent: string; ageNote: string }> = {
  l1: { label: 'Level 1', sublabel: '幼儿版', accent: 'amber', ageNote: '5-7岁 · 彩色绘本' },
  l2: { label: 'Level 2', sublabel: '小学版', accent: 'emerald', ageNote: '6岁+ · 彩色绘本' },
  l3: { label: 'Level 3', sublabel: '初中版', accent: 'cyan', ageNote: '初中 · 黑白插图' },
};
`;
  
  fs.writeFileSync(dataFile, newContent, 'utf8');
  
  console.log(`\nFixed ${fixedStories} stories`);
  console.log(`Cleared ${removedStories} copyright texts`);
  console.log('Data file updated:', dataFile);
}

main().catch(console.error);
